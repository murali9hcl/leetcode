package neetcode;

public class Bitwise {
    public static void main(String[] args) {
        int x = 10;
        int y = 1;
        System.out.println("x & y = " + (x & y));
        System.out.println("x | y = " + (x | y));
        System.out.println("x >> y = " + (x >> y));
        System.out.println("x >>=1 = " + (x >>=1));
        System.out.println("x ^ y = " + (7 ^ 5));
    }
}
